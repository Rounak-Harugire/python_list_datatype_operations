List =['R', 'O', 'U', 'N', 'A', 'K',
        'H', 'A', 'R', 'U', 'G', 'I', 'R', 'E']
print("Initial List: ")
print(List)
Sliced_List = List[:-6]
print("\nElements sliced till 6th element from last: ")
print(Sliced_List)
Sliced_List = List[-6:-1]
print("\nElements sliced from index -6 to -1")
print(Sliced_List)
Sliced_List = List[::-1]
print("\nPrinting List in reverse: ")
print(Sliced_List)
